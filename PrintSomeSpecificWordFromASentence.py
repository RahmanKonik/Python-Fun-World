# python program 02: Find and print out a specific word by using a index number from a sentence.

sentence = "Python logical operators are used to combine or modify conditions"

# index = 0,

print(sentence[0]) # it will be print only 'p'

# index = -1

print(sentence[-1]) # it will be print only 's'

# index = [0:6]

print(sentence[0:6]) # it will be print 'python' word.

# as same we can print any word or letter from a sentence.


