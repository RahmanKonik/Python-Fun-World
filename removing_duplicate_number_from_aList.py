#First python program: removing the all duplicate number from a list.

list_item = [1, 2, 3, 4, 1, 2, 3, 6, 6]

unique_list = []

for number in list_item:

    if number not in unique_list:
        unique_list.append(number)
print(f" Unique list is {unique_list}")

